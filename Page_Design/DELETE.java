    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        String name, email, address, phone, course, fname, branch, university, gender, sem;

        ResultSet rs = Db_operations.showAllStu();
        try {
            if (rs != null) {
                while (rs.next()) {
                    name = rs.getString("name");
                    email = rs.getString("email");
                    address = rs.getString("address");
                    phone = rs.getString("phone");
                    course = rs.getString("course");
                    fname = rs.getString("fname");
                    branch = rs.getString("branch");
                    university = rs.getString("university");
                    gender = rs.getString("gender");
                    sem = rs.getString("sem");
                    row = new Object[]{name, email, address, phone, course, fname, branch, university, gender, sem};
                    dtm.addRow(row);
                }
            }
        } catch (Exception e) {
            System.out.println(e);
        }

    }//GEN-LAST:event_formWindowOpened

    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
      //  String email=null;
      int j=0;
      //  int row = jTable1.getSelectedRow();
        int[] rows = jTable1.getSelectedRows();
        for (int i = 0; i < rows.length; i++) {
             String email= (String) jTable1.getValueAt(rows[i], 1);
              j = Db_operations.delete_details(email);
        
        }
        if (j > 0) {
            JOptionPane.showMessageDialog(rootPane, "deleted successfully");
            new DELETE().setVisible(true);
            setVisible(false);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Not Deleted due to some error", "Update error", JOptionPane.ERROR_MESSAGE);
        }

       
    }//GEN-LAST:event_jButton2MouseClicked


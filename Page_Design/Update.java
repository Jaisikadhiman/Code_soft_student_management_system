    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        String name, email, course, sem;
        ResultSet rs = Db_operations.showAllStu();
        try {
            if (rs != null) {
                while (rs.next()) {
                    name = rs.getString("name");
                    email = rs.getString("email");
                    course = rs.getString("course");
                    sem = rs.getString("sem");
                    row = new Object[]{name, email, course, sem};
                    dtm.addRow(row);

                }
            }
        } catch (Exception e) {
            System.out.println(e);
        }
    }//GEN-LAST:event_formWindowOpened

    private void jtable1mouseclicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtable1mouseclicked
        int row = jTable1.getSelectedRow();
        String selected_email = (String) jTable1.getValueAt(row, 1);
        ResultSet rs = Db_operations.showDetails(selected_email);
        try {
            if (rs != null) {
                while (rs.next()) {
                    jTextField1.setText(rs.getString("name"));
                    jTextField2.setText(rs.getString("email"));
                    jTextField3.setText(rs.getString("address"));
                    jTextField4.setText(rs.getString("phone"));
                    jTextField5.setText(rs.getString("course"));
                    jTextField6.setText(rs.getString("fname"));
                    jTextField7.setText(rs.getString("branch"));
                    jTextField8.setText(rs.getString("university"));
                    jTextField10.setText(rs.getString("sem"));
                    String gender = rs.getString("gender");
                    if (gender.equals("m")) {
                        jRadioButton1.setSelected(true);
                    } else {
                        jRadioButton2.setSelected(true);
                    }
                }
            }
        } catch (Exception e) {
        }

    }//GEN-LAST:event_jtable1mouseclicked


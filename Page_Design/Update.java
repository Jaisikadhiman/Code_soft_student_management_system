    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String name = jTextField1.getText();
        String email = jTextField2.getText();
        String address = jTextField3.getText();
        String phone = jTextField4.getText();
        String course = jTextField5.getText();
        String fname = jTextField6.getText();
        String branch = jTextField7.getText();
        String university = jTextField8.getText();
        String sem = jTextField10.getText();
        String gender="";
        if(jRadioButton1.isSelected()){
            gender="m";
        }else{
            gender="f";
        }
           AddStu ads= new AddStu();
           ads.setName(name);
           ads.setEmail(email);
           ads.setAddress(address);
           ads.setPhone(phone);
           ads.setCourse(course);
           ads.setFname(fname);
           ads.setBranch(branch);
           ads.setUniversity(university);
           ads.setSem(sem);
           ads.setGender(gender);
           int i=Db_operations.update_details(ads);
           if(i>0){
      JOptionPane.showMessageDialog(rootPane, "Details updated Successfully");
      new Update().setVisible(true);
      setVisible(false);
  }else{
     JOptionPane.showMessageDialog(rootPane,"Employee not updated due to some error","Update error", JOptionPane.ERROR_MESSAGE);
  }
     
    }//GEN-LAST:event_jButton1MouseClicked

